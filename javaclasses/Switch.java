package javaclasses;

public class Switch {
	public static void main(String[] args) {
	
	char grade = 'C';
	
	
	
	switch (grade) {
		case 'A':
			System.out.println("This is for grade A");
			break;
		case 'B':
			System.out.println("This is for grade B");
			break;
		case 'C':
			System.out.println("This is for grade C");
			break;
		case 'D':
			System.out.println("This is for grade D");
			break;
	
		default:
			System.out.println("This is for no grade ");
	
	}












}
	
	
	
	
	
	
	
	
	
	
	
	
	
	

}
