package javaclasses;

public class Loop {

	public static void main(String[] args) {
		
		
		
//for loop
	 for(int i=0;i<=100;i++) {
	 System.out.println(i);
	 }
	 
//while loop
	 int i=0;
	 while(i<=5) {
	 System.out.println(i);
	 i++;
	 }
	 
//do while loop
	 int j=0;
	 do{
		 System.out.println(j);
	 j++;
	 }while(j<5); 
	 
	}

}
