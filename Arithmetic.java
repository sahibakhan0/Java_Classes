package javaclasses;

public class Arithmetic {

	public static void main(String[] args) {
    int a = 10; 
    int b = 20;
    int sum = a+b; 
    int sub = b-a;
    int mult = a*b;
    int div = b/a;
    int rem = a%b;
    
    
    
    System.out.println(sum);
    System.out.println(sub);
    System.out.println(mult);
    System.out.println(div);
    System.out.println(rem);
	}

}
